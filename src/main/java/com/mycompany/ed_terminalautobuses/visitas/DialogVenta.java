    private void cargarDatos() {
        cmbOrigen.removeAllItems();
        cmbDestino.removeAllItems();

        for (String p : paradas) {
            cmbOrigen.addItem(p);
            cmbDestino.addItem(p);
        }

        txtPrecio.setEditable(false);
        txtNombre.setText("");
        txtPrecio.setText("");
    }

    private void configurarEventos() {

        btnCancelar.addActionListener(e -> dispose());

        btnConfirmar.addActionListener(e -> confirmarVenta());
    }

    private void confirmarVenta() {
        String nombre = txtNombre.getText().trim();
        String origen = (String) cmbOrigen.getSelectedItem();
        String destino = (String) cmbDestino.getSelectedItem();

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del pasajero");
            return;
        }

        if (origen.equals(destino)) {
            JOptionPane.showMessageDialog(this, "El origen y destino no pueden ser iguales");
            return;
        }

        int precio = calcularPrecio(origen, destino);
        txtPrecio.setText(String.valueOf(precio));

        JOptionPane.showMessageDialog(
            this,
            "Venta registrada:\n" +
            "Pasajero: " + nombre + "\n" +
            "Origen: " + origen + "\n" +
            "Destino: " + destino + "\n" +
            "Precio: $" + precio
        );

        dispose();
    }

    private int calcularPrecio(String origen, String destino) {
        int i1 = java.util.Arrays.asList(paradas).indexOf(origen);
        int i2 = java.util.Arrays.asList(paradas).indexOf(destino);

        int distancia = Math.abs(i1 - i2);

        return 50 + distancia * 30;
    }
    
